describe("annotate-pdf — 좌표 변환 (Konva Y-down → PDF Y-up)", () => {
  it("Y좌표가 올바르게 변환된다 — 결과 PDF가 유효하다", async () => {
    const file = await createMarkedPdf(1);
    const { onProgress } = createProgressTracker();

    // pageHeight = 842, y=100, height=30
    // toPdfY(100, 30, 842) = 842 - 100 - 30 = 712
    const result = await annotatePdf(
      [file],
      { annotations: [makeHighlight({ y: 100, height: 30 })] },
      onProgress,
    );

    const pdf = await resultToPdf(result.blob);
    expect(pdf.getPageCount()).toBe(1);
    // 결과를 다시 저장해도 유효해야 함
    const bytes = await pdf.save();
    expect(bytes.length).toBeGreaterThan(0);
  });

  it("페이지 하단 y=0에 어노테이션 → PDF 상단에 렌더링", async () => {
    const file = await createMarkedPdf(1);
    const { onProgress } = createProgressTracker();

    // y=0, height=30 → toPdfY = 842 - 0 - 30 = 812 (PDF 상단 근처)
    const result = await annotatePdf(
      [file],
      { annotations: [makeHighlight({ y: 0, height: 30 })] },
      onProgress,
    );

    const pdf = await resultToPdf(result.blob);
    expect(pdf.getPageCount()).toBe(1);
  });

  it("페이지 하단 근처에 어노테이션 → PDF 하단에 렌더링", async () => {
    const file = await createMarkedPdf(1);
    const { onProgress } = createProgressTracker();

    // y=810, height=30 → toPdfY = 842 - 810 - 30 = 2 (PDF 하단 근처)
    const result = await annotatePdf(
      [file],
      { annotations: [makeHighlight({ y: 810, height: 30 })] },
      onProgress,
    );

    const pdf = await resultToPdf(result.blob);
    expect(pdf.getPageCount()).toBe(1);
  });
});

