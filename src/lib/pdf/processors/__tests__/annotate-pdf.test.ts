// The processor uses document.createElement("canvas") for sticky notes,
// stamps, and CJK text rendering. We mock it to return a minimal 1x1 PNG.

// Valid 1x1 RGB PNG (generated by Python zlib)
const TINY_PNG = new Uint8Array([
  0x89,0x50,0x4e,0x47,0x0d,0x0a,0x1a,0x0a,0x00,0x00,0x00,0x0d,0x49,0x48,
  0x44,0x52,0x00,0x00,0x00,0x01,0x00,0x00,0x00,0x01,0x08,0x02,0x00,0x00,
  0x00,0x90,0x77,0x53,0xde,0x00,0x00,0x00,0x0c,0x49,0x44,0x41,0x54,0x78,
  0x9c,0x63,0xf8,0xcf,0xc0,0x00,0x00,0x03,0x01,0x01,0x00,0xc9,0xfe,0x92,
  0xef,0x00,0x00,0x00,0x00,0x49,0x45,0x4e,0x44,0xae,0x42,0x60,0x82,
]);

function createMockCanvas() {
  const mockCtx = {
    scale: vi.fn(),
    clearRect: vi.fn(),
    fillRect: vi.fn(),
    strokeRect: vi.fn(),
    fillText: vi.fn(),
    beginPath: vi.fn(),
    moveTo: vi.fn(),
    lineTo: vi.fn(),
    closePath: vi.fn(),
    fill: vi.fn(),
    stroke: vi.fn(),
    translate: vi.fn(),
    measureText: vi.fn(() => ({ width: 50 })),
    font: "",
    fillStyle: "",
    strokeStyle: "",
    lineWidth: 1,
    textAlign: "left",
    textBaseline: "top",
  };

  const mockCanvas = {
    width: 0,
    height: 0,
    getContext: vi.fn(() => mockCtx),
    toBlob: vi.fn((cb: (b: Blob) => void) => {
      cb(new Blob([TINY_PNG], { type: "image/png" }));
    }),
  };

  return mockCanvas;
}

beforeEach(() => {
  // Mock document.createElement for canvas (Node has no DOM)
  const mockDoc = {
    createElement: vi.fn((tag: string) => {
      if (tag === "canvas") {
        return createMockCanvas() as unknown as HTMLCanvasElement;
      }
      return {} as HTMLElement;
    }),
  };
  if (typeof globalThis.document === "undefined") {
    (globalThis as Record<string, unknown>).document = mockDoc;
  } else {
    vi.spyOn(document, "createElement").mockImplementation(mockDoc.createElement);
  }
});

