/** Compare byte length of result to original — annotations should add content */
async function getOriginalByteLength(file: File): Promise<number> {
  const origPdf = await PDFDocument.load(await file.arrayBuffer());
  const origBytes = await origPdf.save();
  return origBytes.length;
}

