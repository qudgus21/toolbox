let _counter = 0;
function baseProps(
  type: string,
  pageIndex = 0,
  overrides: Partial<AnnotateElement> = {},
) {
  return {
    id: `test_${++_counter}`,
    type,
    pageIndex,
    x: 50,
    y: 100,
    width: 200,
    height: 30,
    rotation: 0,
    opacity: 1,
    ...overrides,
  };
}

function makeHighlight(overrides: Partial<HighlightElement> = {}): HighlightElement {
  return {
    ...baseProps("highlight"),
    color: "yellow",
    ...overrides,
  } as HighlightElement;
}

